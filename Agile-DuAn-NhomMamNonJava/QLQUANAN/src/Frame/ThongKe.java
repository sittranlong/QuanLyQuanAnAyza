    private void btn_thongkeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_thongkeActionPerformed
        // TODO add your handling code here:
        if (tf_batdau.getText().trim().isEmpty() || tf_ketthuc.getText().trim().isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng nhập khoảng năm cần tìm!", "Thông báo");
            return;
        }
        int batdau;
        int ketthuc;
        try {
            batdau = Integer.parseInt(tf_batdau.getText().trim());
            ketthuc = Integer.parseInt(tf_ketthuc.getText().trim());
        } catch (Exception e) {
            System.out.println(e);
            ShowDialog.openMessage(this, "Vui lòng nhập đúng định dạng!", "Thông báo");
            return;
        }
        if (batdau > ketthuc || batdau < 2000 || (ketthuc > Integer.parseInt(cbb_nam.getItemAt(0)))) {
            ShowDialog.openMessage(this, "Không thống kê được!", "Thông báo");
            return;
        }
        fillTableNam(batdau, ketthuc);
        setDoanhThuNam();
    }//GEN-LAST:event_btn_thongkeActionPerformed

