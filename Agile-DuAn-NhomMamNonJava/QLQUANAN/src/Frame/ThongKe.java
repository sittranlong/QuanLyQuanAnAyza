    public void fillTableThang(int nam) {
        DefaultTableModel dtm = (DefaultTableModel) tb_thang.getModel();
        dtm.setRowCount(0);
        try {
            Connection conn = DatabaseHelper.getConnection();
            String sql = "SELECT * FROM dbo.f_thongkethang(?)";
            CallableStatement cs = conn.prepareCall(sql);

            cs.setInt(1, nam);

            ResultSet rs = cs.executeQuery();

            while (rs.next()) {
                dtm.addRow(new Object[]{rs.getInt(1), rs.getInt(2), rs.getInt(3)});
            }

            conn.close();
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    public void setDoanhThuThang() {
        DefaultTableModel dtm = (DefaultTableModel) tb_thang.getModel();
        int doanhthu = 0;
        for (int i = 0; i < 12; i++) {
            doanhthu += (int) dtm.getValueAt(i, 2);
        }
        lb_tongdoanhthuthang.setText(Formater.formatVND(doanhthu));
    }

    public void fillTableNam(int batdau, int ketthuc) {
        DefaultTableModel dtm = (DefaultTableModel) tb_nam.getModel();
        dtm.setRowCount(0);
        try {
            Connection conn = DatabaseHelper.getConnection();
            String sql = "SELECT * FROM dbo.f_thongkenam(?, ?)";
            CallableStatement cs = conn.prepareCall(sql);

            cs.setInt(1, batdau);
            cs.setInt(2, ketthuc);

            ResultSet rs = cs.executeQuery();

            while (rs.next()) {
                dtm.addRow(new Object[]{rs.getInt(1), rs.getInt(2), rs.getInt(3)});
            }

            conn.close();
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    public void setDoanhThuNam() {
        DefaultTableModel dtm = (DefaultTableModel) tb_nam.getModel();
        int doanhthu = 0;
        for (int i = 0; i < tb_nam.getRowCount(); i++) {
            doanhthu += (int) dtm.getValueAt(i, 2);
        }
        lb_doanhthunam.setText(Formater.formatVND(doanhthu));
    }

