        if (diachi.isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng nhập địa chỉ nhân viên!", "Thông báo");
            return;
        }
        String matkhau = tf_matkhau.getText().trim();
        if (matkhau.isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng nhập mật khẩu nhân viên!", "Thông báo");
            return;
        }
        StaffDAO.updateStaff(id, hoten, sdt, gioitinh, diachi, matkhau);
        loadList();
        fillTable();
        clearForm();
        ShowDialog.openMessage(this, "Sửa thành công!", "Thông báo");

