    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        String hoten = tf_hoten.getText().trim();
        if (hoten.isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng nhập họ tên nhân viên!", "Thông báo");
            return;
        }
        String sdt = tf_sdt.getText().trim();
        if (sdt.isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng nhập số điện thoại nhân viên!", "Thông báo");
            return;
        }
        int gioitinh = rbtn_nam.isSelected() ? 1 : 0;
        String diachi = ta_diachi.getText().trim();
        if (diachi.isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng nhập địa chỉ nhân viên!", "Thông báo");
            return;
        }
        String taikhoan = tf_taikhoan.getText().trim();
        if (taikhoan.isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng nhập tài khoản nhân viên!", "Thông báo");
            return;
        }
        if (checkUsername(taikhoan) == 1) {
            ShowDialog.openMessage(this, "Tài khoản đã tồn tại!", "Thông báo");
            return;
        }
        String matkhau = tf_matkhau.getText().trim();
        if (matkhau.isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng nhập mật khẩu nhân viên!", "Thông báo");
            return;
        }
        StaffDAO.insertStaff(hoten, sdt, gioitinh, diachi, taikhoan, matkhau);
        loadList();
        fillTable();
        ShowDialog.openMessage(this, "Thêm thành công!", "Thông báo");
        clearForm();
    }//GEN-LAST:event_btn_themActionPerformed

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        if (tf_ma.getText().isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng chọn nhân viên cần xóa!", "Thông báo");
            return;
        }
        int id = Integer.parseInt(tf_ma.getText());
        StaffDAO.deleteStaff(id);
        String username = searchUsername(id);
        int i = ShowDialog.showConfirm(this, "Bạn có muốn xóa nhân viên?", "Xác nhận");
        if (i == JOptionPane.YES_OPTION) {
            if (username != null) {
                UserDAO.deleteUser(username);
            }
            loadList();
            fillTable();
            clearForm();
            ShowDialog.openMessage(this, "Xóa thành công!", "Thông báo");
        }
    }//GEN-LAST:event_btn_xoaActionPerformed

