    public void loadList() {
        listS = StaffDAO.getList();
        listU = UserDAO.getList();
    }

    public void fillTable() {
        DefaultTableModel dtm = (DefaultTableModel) tb_fill.getModel();
        dtm.setRowCount(0);
        for (Staff l : listS) {
            String gioitinh = l.getGioitinh() == 1 ? "Nam" : "Nữ";
            dtm.addRow(new Object[]{l.getId(), l.getHoten(), l.getSdt(), gioitinh, l.getDiachi(), l.getTaikhoan(), l.getMatkhau()});
        }
    }

    public int checkUsername(String username) {
        for (User l : listU) {
            if (l.getUsername().equalsIgnoreCase(username)) {
                return 1;
            }
        }
        return 0;
    }

    public void clearForm() {
        tf_ma.setText("");
        tf_hoten.setText("");
        tf_sdt.setText("");
        tf_matkhau.setText("");
        tf_taikhoan.setText("");
        rbtn_nam.setSelected(true);
        ta_diachi.setText("");
    }

    public void fillForm() {
        int i = tb_fill.getSelectedRow();
        tf_ma.setText(String.valueOf(listS.get(i).getId()));
        tf_hoten.setText(listS.get(i).getHoten());
        tf_sdt.setText(listS.get(i).getSdt());
        if (listS.get(i).getGioitinh() == 1) {
            rbtn_nam.setSelected(true);
        } else {
            rbtn_nu.setSelected(true);
        }
        ta_diachi.setText(listS.get(i).getDiachi());
        tf_taikhoan.setText(listS.get(i).getTaikhoan());
        tf_matkhau.setText(listS.get(i).getMatkhau());
    }

    public String searchUsername(int id) {
        for (Staff l : listS) {
            if (l.getId() == id) {
                return l.getTaikhoan();
            }
        }
        return null;
    }

