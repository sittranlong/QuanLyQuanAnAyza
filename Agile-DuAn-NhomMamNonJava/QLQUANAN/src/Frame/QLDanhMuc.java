    public void selectFirstRow() {
        if (tb_fill.getRowCount() > 0) {
            tb_fill.setRowSelectionInterval(0, 0);
            fillForm();
        }
    }

    public void disnableBtn() {
        btn_them.setEnabled(false);
        btn_sua.setEnabled(false);
        btn_xoa.setEnabled(false);

    }

    public void loadList() {
        listCa = CategoryDAO.getList();
        listP = ProductDAO.getList();
    }

    public void fillTable() {
        DefaultTableModel dtm = (DefaultTableModel) tb_fill.getModel();
        dtm.setRowCount(0);
        for (Category l : listCa) {
            dtm.addRow(new Object[]{l.getId(), l.getTendanhmuc()});
        }
    }

    public void fillForm() {
        int i = tb_fill.getSelectedRow();
        tf_madanhmuc.setText(String.valueOf(listCa.get(i).getId()));
        tf_tendanhmuc.setText(listCa.get(i).getTendanhmuc());
    }

    public void deleteProduct(int id_cate) {
        for (Product l : listP) {
            if (id_cate == l.getId_category()) {
                ProductDAO.deleteProduct(l.getId());
            }
        }
    }

    public void clearForm() {
        tf_madanhmuc.setText("");
        tf_tendanhmuc.setText("");
    }

    public boolean checkTenDanhMuc(String ten) {
        DefaultTableModel dtm = (DefaultTableModel) tb_fill.getModel();
        for (int i = 0; i < dtm.getRowCount(); i++) {
            if (ten.equalsIgnoreCase((String) dtm.getValueAt(i, 1))) {
                return true;
            }
        }
        return false;
    }

