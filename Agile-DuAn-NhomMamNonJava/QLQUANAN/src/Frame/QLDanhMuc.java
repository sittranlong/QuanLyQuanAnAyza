    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        String tendanhmuc = tf_tendanhmuc.getText().trim();
        if (tendanhmuc.isEmpty()) {
            ShowDialog.openMessage(this, "Tên danh mục không hợp lệ!", "Thông báo");
            return;
        }
        if (checkTenDanhMuc(tendanhmuc)) {
            ShowDialog.openMessage(this, "Danh mục đã tồn tại!", "Thông báo");
            return;
        }
        CategoryDAO.insertCategory(tf_tendanhmuc.getText());
        loadList();
        fillTable();
        clearForm();
        ShowDialog.openMessage(this, "Thêm danh mục thành công!", "Thông báo");
    }//GEN-LAST:event_btn_themActionPerformed

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // TODO add your handling code here:
        if (tf_madanhmuc.getText().isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng chọn danh mục!", "Thông báo");
            return;
        }
        int id = Integer.parseInt(tf_madanhmuc.getText());
        String tendanhmuc = tf_tendanhmuc.getText().trim();
        if (tendanhmuc.isEmpty()) {
            ShowDialog.openMessage(this, "Tên danh mục không hợp lệ!", "Thông báo");
            return;
        }
        if (checkTenDanhMuc(tendanhmuc)) {
            ShowDialog.openMessage(this, "Danh mục đã tồn tại!", "Thông báo");
            return;
        }
        CategoryDAO.updateCategory(id, tendanhmuc, "Đang hoạt động");
        loadList();
        fillTable();
        clearForm();
        ShowDialog.openMessage(this, "Sửa thành công!", "Thông báo");

    }//GEN-LAST:event_btn_suaActionPerformed

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        loadList();
        if (tf_madanhmuc.getText().isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng chọn danh mục!", "Thông báo");
            return;
        }
        int i = ShowDialog.showConfirm(this, "Bạn có muốn xóa danh mục đã chọn?", "Xác nhận");
        if (i == JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(tf_madanhmuc.getText());
            CategoryDAO.deleteCategory(id, "Không hoạt động");
            deleteProduct(id);
            loadList();
            fillTable();
            clearForm();
            ShowDialog.openMessage(this, "Xóa thành công!", "Thông báo");
        } else {
            ShowDialog.openMessage(this, "Hủy xóa!", "Thông báo");

        }
    }//GEN-LAST:event_btn_xoaActionPerformed


