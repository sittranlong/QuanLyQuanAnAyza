    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        String hoten = tf_ten.getText().trim();
        String sdt = tf_sdt.getText().trim();
        String diachi = ta_diachi.getText().trim();
        int gioitinh = rbtn_nam.isSelected() ? 1 : 0;
        if (hoten.isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng nhập họ tên khách hàng!", "Thông báo");
            return;
        }
        if (sdt.isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng nhập số điện thoại khách hàng!", "Thông báo");
            return;
        }
        if (diachi.isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng nhập địa chỉ khách hàng!", "Thông báo");
            return;
        }
        CustomerDAO.insertCustomer(hoten, sdt, gioitinh, diachi);
        loadList();
        fillTable();
        clearForm();
        ShowDialog.openMessage(this, "Thêm thành công!", "Thông báo");

    }//GEN-LAST:event_btn_themActionPerformed

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // TODO add your handling code here:
        String hoten = tf_ten.getText().trim();
        String sdt = tf_sdt.getText().trim();
        String diachi = ta_diachi.getText().trim();
        if (tf_ma.getText().isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng chọn khách hàng!", "Thông báo");
            return;
        }
        int id = Integer.parseInt(tf_ma.getText());
        int gioitinh = rbtn_nam.isSelected() ? 1 : 0;
        if (hoten.isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng nhập họ tên khách hàng!", "Thông báo");
            return;
        }
        if (sdt.isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng nhập số điện thoại khách hàng!", "Thông báo");
            return;
        }
        if (diachi.isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng nhập địa chỉ khách hàng!", "Thông báo");
            return;
        }
        CustomerDAO.updateCustomer(hoten, sdt, gioitinh, diachi, "Đang hoạt động", id);
        loadList();
        fillTable();
        clearForm();
        ShowDialog.openMessage(this, "Sửa thành công!", "Thông báo");

    }//GEN-LAST:event_btn_suaActionPerformed

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        String hoten = tf_ten.getText().trim();
        String sdt = tf_sdt.getText().trim();
        String diachi = ta_diachi.getText().trim();
        if (tf_ma.getText().isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng chọn khách hàng!", "Thông báo");
            return;
        }
        int id = Integer.parseInt(tf_ma.getText());
        int gioitinh = rbtn_nam.isSelected() ? 1 : 0;
        int i = ShowDialog.showConfirm(this, "Bạn có muốn xóa khách hàng?", "Xác nhận");
        if (i == JOptionPane.YES_OPTION) {
            CustomerDAO.updateCustomer(hoten, sdt, gioitinh, diachi, "Không hoạt động", id);
            clearForm();
            loadList();
            fillTable();
            clearForm();
            ShowDialog.openMessage(this, "Xóa thành công!", "Thông báo");
        }
    }//GEN-LAST:event_btn_xoaActionPerformed

    private void btn_timkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_timkiemActionPerformed
        // TODO add your handling code here:
        String sdt = tf_sodienthoai.getText().trim();
        if (sdt.isEmpty()) {
            ShowDialog.openMessage(this, "Vui lòng nhập số điện thoại khách hàng cần tìm!", "Thông báo");
            return;
        }
        int indexRow = timKhachHang(sdt);
        if (indexRow == -1) {
            ShowDialog.openMessage(this, "Không tìm thấy khách hàng!", "Thông báo");
            return;
        } else {
            tb_fill.setRowSelectionInterval(indexRow, indexRow);
            fillForm();
        }
    }//GEN-LAST:event_btn_timkiemActionPerformed


