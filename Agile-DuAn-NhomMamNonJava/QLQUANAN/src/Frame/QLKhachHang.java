    public void loadList() {
        listC = CustomerDAO.getList();
    }

    public void fillTable() {
        DefaultTableModel dtm = (DefaultTableModel) tb_fill.getModel();
        dtm.setRowCount(0);
        for (Customer l : listC) {
            String gioitinh = l.getGioitinh() == 1 ? "Nam" : "Nữ";
            dtm.addRow(new Object[]{l.getId(), l.getHoten(), l.getSdt(), gioitinh, l.getDiachi()});
        }
    }

    //hiển thị thông tin khách hàng lên form khi chọn trong bảng
    public void fillForm() {
        int index = tb_fill.getSelectedRow();
        tf_ma.setText(String.valueOf(listC.get(index).getId()));
        tf_ten.setText(listC.get(index).getHoten());
        tf_sdt.setText(listC.get(index).getSdt());
        if (listC.get(index).getGioitinh() == 1) {
            rbtn_nam.setSelected(true);
        } else {
            rbtn_nu.setSelected(true);
        }
        ta_diachi.setText(listC.get(index).getDiachi());
    }

    //làm mới form
    public void clearForm() {
        tf_ma.setText("");
        tf_ten.setText("");
        tf_sdt.setText("");
        rbtn_nam.setSelected(true);
        ta_diachi.setText("");
        tf_sodienthoai.setText("");
    }
    
    public int timKhachHang(String sdt) {
        for (Customer l : listC) {
            if (l.getSdt().equals(sdt)) {
                return listC.indexOf(l);
            }
        }
        return -1;
    }

