    private void pf_passKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pf_passKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String user = tf_user.getText().trim();
            String pass = pf_pass.getText();
            if (user.isEmpty()) {
                ShowDialog.openMessage(this, "Vui lòng nhập username!", "Thông báo");
                return;
            }
            if (pass.isEmpty()) {
                ShowDialog.openMessage(this, "Vui lòng nhập password!", "Thông báo");
                return;
            }
            if (checkUser() == 0) {
                ShowDialog.openMessage(this, "Người dùng không tồn tại!", "Thông báo");
                return;
            }
            for (User l : listU) {
                if (l.getUsername().equals(user) && l.getPassword().equals(pass)) {
                    ShowDialog.openMessage(this, "Đăng nhập thành công!", "Thông báo");
                    this.setVisible(false);
                    main = new Main(l);
                }
            }
        }
    }//GEN-LAST:event_pf_passKeyPressed

