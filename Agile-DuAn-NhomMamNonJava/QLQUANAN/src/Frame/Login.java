    private void btn_dangnhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_dangnhapActionPerformed
        // TODO add your handling code here:
        try {
            String user = tf_user.getText().trim();
            String pass = pf_pass.getText();
            if (user.isEmpty()) {
                ShowDialog.openMessage(this, "Tên đăng nhập không hợp lệ!", "Thông báo");
                return;
            }
            if (pass.isEmpty()) {
                ShowDialog.openMessage(this, "Mật khẩu không hợp lệ!", "Thông báo");
                return;
            }
            
            if (checkUser() == 0) {
                ShowDialog.openMessage(this, "Đăng nhập không thành công!", "Thông báo");
                return;
            }
            for (User l : listU) {
                if (l.getUsername().equalsIgnoreCase(user) && l.getPassword().equals(pass)) {
                    ShowDialog.openMessage(this, "Đăng nhập thành công!", "Thông báo");
                    this.setVisible(false);
                    main = new Main(l);
                }
            }
            ShowDialog.openMessage(this, "Đăng nhập thất bại!", "Thông báo");
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btn_dangnhapActionPerformed

