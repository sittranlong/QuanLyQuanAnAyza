    public void loadList() {
        try {
            listU = new ArrayList<>();
            Connection con = DatabaseHelper.getConnection();
            String sql = "SELECT * FROM [USER]";
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                listU.add(new User(rs.getString("Username"), rs.getString("Password"), rs.getString("Role")));
                System.out.println(rs.getString("Username") + " " + rs.getString("Password") + " " + rs.getString("Role"));
            }

            con.close();
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    public int checkUser() {
        for (User l : listU) {
            if (tf_user.getText().trim().equals(l.getUsername())) {
                return 1;
            }
        }
        return 0;
    }

    public int checkPass() {
        for (User l : listU) {
            if (pf_pass.getText().equals(l.getPassword())) {
                return 1;
            }
        }
        return 0;
    }

